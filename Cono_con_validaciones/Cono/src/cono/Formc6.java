    private void jConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jConsultarActionPerformed

        DecimalFormat formatoN = new DecimalFormat("#,###,###.##");        
        float r = Float.parseFloat(tfRadio.getText());
        float h = Float.parseFloat(tfAltura.getText());
        float g = Float.parseFloat(tfGeneratriz.getText());
        Cono c = new Cono();
        c.setAltura(h);
        c.setGeneratriz(g);
        c.setRadio(r);
        
        tfVolumen.setText(String.valueOf(formatoN.format(c.obtenerVolumen())));
        tfArea.setText(String.valueOf(formatoN.format(c.obtenerArea())));
       
    }//GEN-LAST:event_jConsultarActionPerformed

