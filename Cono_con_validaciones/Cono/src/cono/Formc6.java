    //Lectura Caracter a Caracter, valida que solamente se ingresen números
    private void tfRadioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfRadioKeyTyped
        // Valida caracter a caracter que esté dentro del rango númerico según norma ASCII
        int k = (int) evt.getKeyChar();
        try{
        if(k>=97 && k<=122 || k>=65 && k<=90){
            evt.setKeyChar((char) KeyEvent.VK_CLEAR);
            lMensaje.setText("Favor ingrese solo números en radio");
        }
        }catch(ClassCastException  e){
            lMensaje.setText("Datos no validos");
            
        }
    }//GEN-LAST:event_tfRadioKeyTyped

